    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        try {
            // TODO add your handling code here:
            DAO oDao = new DAO();
            Trabajador oTrabajador;
            oTrabajador = oDao.isTrabajador(txtRutT.getText(), txtClaveT.getText());
            if (oTrabajador != null) {
                JOptionPane.showMessageDialog(rootPane, "Bienvenido " + oTrabajador.getNombre());
                Solicitudes oSolicitudes = new Solicitudes(txtRutT.getText());
                oSolicitudes.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al ingresar \n 1.- El usuario no existe "
                        + "\n 2.- Uno de los campos es incorrecto");
                txtRutT.setText("");
                txtClaveT.setText("");
                txtRutT.requestFocus();
            }

        } catch (SQLException ex) {
            Logger.getLogger(LogTrabajador.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIngresarActionPerformed

